/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
    switch(motor_state) {
      case MOTOR_ACCEL:  // fast blink
        HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_9);
        osDelay(100);
        break;
      case MOTOR_CRUISE:  // medium blink
        HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_9);
        osDelay(300);
        break;
      case MOTOR_DECEL:  // slow blink
        HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_9);
        osDelay(700);
        break;
      case MOTOR_STOP:  // solid on
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_9, GPIO_PIN_SET);
        osDelay(500);
        break;
      default:  // solid off
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_9, GPIO_PIN_RESET);
        osDelay(500);
        break;
    }
  }
  /* USER CODE END 5 */
}

/**
  * @brief  Period elapsed callback in non blocking mode
  * @note   This function is called  when TIM6 interrupt took place, inside
  * HAL_TIM_IRQHandler(). It makes a direct call to HAL_IncTick() to increment
  * a global variable "uwTick" used as application time base.
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* USER CODE BEGIN Callback 0 */
  /* USER CODE END Callback 0 */
  if (htim->Instance == TIM6)
  {
    HAL_IncTick();
  }
  /* USER CODE BEGIN Callback 1 */
  if (htim->Instance == TIM6)
  {
        switch (motor_state)
        {
          case MOTOR_ACCEL:
      current_speed_hz += accel_rate;
      if (current_speed_hz >= cruise_speed_hz) {
        current_speed_hz = cruise_speed_hz;
        motor_state = MOTOR_CRUISE;
      }
      Set_Motor_Speed_Hz(current_speed_hz);
      break;

    case MOTOR_CRUISE:
      if (pulse_count >= target_pulses - decel_pulses)
        motor_state = MOTOR_DECEL;
      break;

    case MOTOR_DECEL:
      if (current_speed_hz <= start_speed_hz + decel_rate) {
        current_speed_hz = 0;
        HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_1);
        __HAL_TIM_DISABLE(&htim4);
        motor_state = MOTOR_STOP;
      } else {
        current_speed_hz -= decel_rate;
        Set_Motor_Speed_Hz(current_speed_hz);
      }
      break;

      case MOTOR_IDLE:
      case MOTOR_STOP:
      default:
        break;
    }
  }
  /* USER CODE END Callback 1 */
}

