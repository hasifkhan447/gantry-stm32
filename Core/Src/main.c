/* USER CODE BEGIN 4 */
void Motor_Start(void)
{
  if (motor_state == MOTOR_IDLE || motor_state == MOTOR_STOP) {
    pulse_count = 0;  // reset count on each new move
    current_speed_hz = start_speed_hz;
    Set_Motor_Speed_Hz(current_speed_hz);
    HAL_TIM_PWM_Start_IT(&htim4, TIM_CHANNEL_1);  // _IT not _Start
    motor_state = MOTOR_ACCEL;
  }
}

void Motor_Reset(void)
{
  HAL_TIM_PWM_Stop_IT(&htim4, TIM_CHANNEL_1);  // match with _IT
  current_speed_hz = 0;
  motor_state = MOTOR_IDLE;
}

void Set_Motor_Speed_Hz(uint32_t target_freq_hz)
{
    // Prevent speeds below our 122 Hz limit or division by zero
    if (target_freq_hz < 123)
    {
        __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, 0); // 0% duty cycle (Stop)
        return;
    }

    // Calculate the new ARR value for an 8 MHz clock and PSC = 0
    uint32_t new_arr = (8000000 / target_freq_hz) - 1;

    // Update the Auto-Reload Register
    __HAL_TIM_SET_AUTORELOAD(&htim4, new_arr);

    // Keep duty cycle at 50% for clean step pulses
    __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, new_arr / 2);
}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM4)
  {
    pulse_count++;
  }
}
